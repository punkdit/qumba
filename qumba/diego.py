from string import ascii_letters
from qumba.qcode import QCode, strop

def dump(code):
    print("#", code)
    n = code.n
    H = strop(code.H, "I")
    L = strop(code.L, "I")
    ops = []
    names = [s+str(i) for i in range(10) for s in ascii_letters]
    mkop = lambda h: {idx:s for (idx,s) in enumerate(h) if s!="I"}
    
    print("code_stabilizers =[")
    for i,h in enumerate(H.split()):
        pre = "X" if "X" in h else "Z"
        line = {"name":pre+names[i], "phase":+1, "paulis":mkop(h)}
        print(line, end=",\n")
    print("]")
    
    print("logical_operators =[")
    for i,h in enumerate(L.split()):
        line = {"name":"L"+names[i], "phase":+1, "paulis":mkop(h)}
        print(line, end=",\n")
    print("]")

