def get_code(name, **kw):

    if name == "gallagher":
        l = argv.get("l", 3) # column weight
        m = argv.get("m", 4) # row weight
        n = argv.get("n", 8) # cols
        r = argv.get("r", n*l//m) # rows
        d = argv.get("d", 0) # distance
        H1 = make_gallagher(r, n, l, m, d)
        #print(shortstr(H1))
        print("H1:", H1.shape)

    elif name == 'hamming':
        H1 = parse("""
        ...1111
        .11..11
        1.1.1.1
        """)

    else:
        H1 = db.get(name)

    if H1 is None:
        return

    H1 = H1.astype(lin.int_scalar)

    n = H1.shape[1]
    k = n-H1.shape[0]
    d = None
    if k < 25:
        d = classical_distance(H1)
    print("[%d, %d, %s]" % (n, k, d))
    girth = find_girth(H1)
    print("girth =", girth)
    print("rank =", lin.rank(H1))
    #print(repr(H1))
    H2 = H1.transpose()
    Hx, Hz, Lx, Lz = hypergraph_product(H1, H2)

    P = lin.get_reductor(Hz)
    Lz = dot2(P, Lz.transpose()).transpose()
    Lz = lin.linear_independent(Lz)

    check = argv.check
    build = argv.build

    if build:
        code = CSSCode(Hx=Hx, Hz=Hz, check=True, build=True, logops_only=False)
    elif check:
        code = CSSCode(Hx=Hx, Hz=Hz, check=False, build=check, logops_only=True)
    else:
        code = CSSCode(Hx=Hx, Hz=Hz, Lx=Lx, Lz=Lz, check=False, build=False)

    if check:
        # check Lz
        assert dot2(Hx, Lz.transpose()).sum() == 0
    
        #print()
        #print("code.Lz:")
        #print(shortstr(code.Lz))
        #print("Lz:")
        #print(shortstr(Lz))
    
        A = lin.solve(code.Lz.transpose(), Lz.transpose())
        assert A is not None
        A = lin.solve(Lz.transpose(), code.Lz.transpose())
        assert A is not None

    return code


