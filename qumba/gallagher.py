def classical_distance(H, max_dist=0):
    n = H.shape[1]
    dist = n
    K = lin.kernel(H)
    K = numpy.array(K)
    Kt = K.transpose()
    for u in numpy.ndindex((2,)*K.shape[0]):
        v = dot2(Kt, u)
        if 0 < v.sum() < dist:
            dist = v.sum()
            #if dist<=max_dist:
            #    break
    return dist


def make_gallagher(r, n, l, m, distance=0, verbose=False):
    assert r%l == 0
    assert n%m == 0
    assert r*m == n*l
    if verbose:
        print("make_gallagher", r, n, l, m, distance)
    H = zeros2(r, n)
    H1 = zeros2(r//l, n)
    H11 = identity2(r//l)
    #print(H1)
    #print(H11)
    for i in range(m):
        H1[:, (n//m)*i:(n//m)*(i+1)] = H11
    #print(shortstrx(H1))

    while 1:
        H2 = H1.copy()
        idxs = list(range(n))
        for i in range(l):
            H[(r//l)*i:(r//l)*(i+1), :] = H2
            shuffle(idxs)
            H2 = H2[:, idxs]
        Hli = lin.linear_independent(H)
        k = Hli.shape[0] - Hli.shape[1]
        assert k <= 24, "ummm, too big? k = %d" % k
        if distance is None:
            break
        if verbose:
            write("/")
        dist = classical_distance(Hli, distance)
        if dist >= distance:
            break
        if verbose:
            write(".")
    if verbose:
        write("\n")
    return Hli


def find_girth(H):
    m, n = H.shape
    for i in range(m):
      for j in range(i+1, m):
        v = tuple(H[i] + H[j])
        if v.count(2) > 1:
            return 4 # found a 4-cycle
    for i in range(m):
      for j in range(i+1, m):
       for k in range(j+1, m):
        v = tuple(H[i] + H[j] + H[k])
        if v.count(2) > 2:
            return 6 # found a 6-cycle



